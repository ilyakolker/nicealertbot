import fetch from 'node-fetch';
import { HttpsProxyAgent } from 'https-proxy-agent';
import { OrefAlert, CityOption, OrefHistoryEntry } from './types';

const OREF_HEADERS: Record<string, string> = {
  'X-Requested-With': 'XMLHttpRequest',
  'Referer': 'https://www.oref.org.il/',
  'Accept': 'application/json',
  'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36',
};

function getProxyAgent(): HttpsProxyAgent<string> {
  const { BRIGHTDATA_HOST, BRIGHTDATA_PORT, BRIGHTDATA_USER, BRIGHTDATA_PASS } = process.env;
  const url = `http://${BRIGHTDATA_USER}:${BRIGHTDATA_PASS}@${BRIGHTDATA_HOST}:${BRIGHTDATA_PORT}`;
  return new HttpsProxyAgent(url);
}

let allCities: { label: string; value: string }[] = [];

export async function loadCities(): Promise<void> {
  try {
    const res = await fetch(
      'https://alerts-history.oref.org.il/Shared/Ajax/GetCitiesMix.aspx?lang=he',
      { headers: OREF_HEADERS, agent: getProxyAgent() }
    );
    if (res.ok) {
      const data = (await res.json()) as any[];
      if (Array.isArray(data)) {
        allCities = data.map((item: any) => ({
          label: item.label_he ?? item.label ?? String(item),
          value: item.label_he ?? item.label ?? String(item),
        }));
        console.log(`✅ Loaded ${allCities.length} cities from Oref`);
        return;
      }
    }
  } catch (err) {
    console.error('Failed to load cities from Oref:', err);
  }
  // Fallback to static list
  allCities = STATIC_AREAS.map(a => ({ label: a, value: a }));
  console.warn(`⚠️ Using static fallback (${allCities.length} cities)`);
}

export async function fetchOrefAlert(): Promise<OrefAlert | null> {
  try {
    const res = await fetch(
      'https://www.oref.org.il/WarningMessages/alert/alerts.json',
      { headers: OREF_HEADERS, agent: getProxyAgent() }
    );
    const text = await res.text();
    if (!text || text.trim() === '' || text.trim() === '{}') return null;
    try {
      const data = JSON.parse(text);
      if (!data?.id || !Array.isArray(data.data) || data.data.length === 0) return null;
      return data as OrefAlert;
    } catch {
      return null;
    }
  } catch (err) {
    console.error('fetchOrefAlert error:', err);
    return null;
  }
}

export async function fetchAlertHistory(): Promise<OrefHistoryEntry[]> {
  try {
    const res = await fetch(
      'https://alerts-history.oref.org.il/Shared/Ajax/GetAlarmsHistory.aspx?lang=he&mode=last24hours',
      { headers: OREF_HEADERS, agent: getProxyAgent() }
    );
    if (res.ok) {
      const data = (await res.json()) as OrefHistoryEntry[];
      if (Array.isArray(data)) return data;
    }
  } catch (err) {
    console.error('fetchAlertHistory error:', err);
  }
  return [];
}

export async function searchOrefCities(query: string): Promise<CityOption[]> {
  if (query.length < 2) return [];
  const q = query.toLowerCase();
  return allCities
    .filter(a => a.label.toLowerCase().includes(q) || a.label.includes(query))
    .slice(0, 8);
}

